# --- 10진수 '정수'를 다른 진법의 '문자열'로 변환 ---

# 기준이 될 10진수 숫자를 변수에 저장합니다.
num = 167

# 각 진법의 문자열로 변환합니다.
# 결과 문자열에는 각 진법을 나타내는 접두어('0b', '0x', '0o')가 자동으로 포함됩니다.
binary_str = bin(num)
hex_str = hex(num)
oct_str = oct(num)

print(f"167의 2진수 표현: {binary_str}")  # 0b10100111
print(f"167의 16진수 표현: {hex_str}")  # 0xa7
print(f"167의 8진수 표현: {oct_str}")  # 0o247

# 만약 접두어를 제거하고 순수한 숫자 부분만 보고 싶다면,
# 문자열 슬라이싱을 활용하여 2번째 인덱스부터 끝까지 잘라냅니다.
print(f"접두어 제거 후: {binary_str[2:]}")  # 10100111
