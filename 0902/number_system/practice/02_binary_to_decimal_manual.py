# --- 2진수 문자열을 10진수로 변환하는 원리 구현 ---

# 변환하고 싶은 2진수를 문자열 형태로 변수에 저장합니다.
binary_str = '1101'
# 최종 10진수 값을 누적하여 저장할 변수입니다.
decimal_result = 0

# 2진수 문자열의 각 문자('1', '1', '0', '1')를 왼쪽부터 순서대로 순회합니다.
for bit in binary_str:
    # 1단계: 기존 결과값에 2를 곱하여 자릿수를 하나씩 왼쪽으로 옮기는 효과를 줍니다.
    decimal_result *= 2

    # 2단계: 현재 자릿수가 '1'이면, 결과값에 1을 더합니다.
    if bit == '1':
        decimal_result += 1

# 최종 변환 결과를 출력합니다.
print(f"2진수 '1101'을 10진수로 변환: {decimal_result}")
