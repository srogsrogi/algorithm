# --- 10진수 '정수'를 다른 진법의 '문자열'로 변환 ---

# 기준이 될 10진수 숫자를 변수에 저장합니다.
num = 167

# 10진수 167을 2진수 문자열('0b...')로 변환합니다.
binary_str = bin(num)
# 10진수 167을 16진수 문자열('0x...')로 변환합니다.
hex_str = hex(num)
# 10진수 167을 8진수 문자열('0o...')로 변환합니다.
oct_str = oct(num)

print(f"167의 2진수 표현: {binary_str}")
print(f"167의 16진수 표현: {hex_str}")
print(f"167의 8진수 표현: {oct_str}")

# 접두어를 제거하고 순수한 숫자 부분만 보고 싶다면, 슬라이싱을 활용합니다.
print(f"접두어 제거 후: {binary_str[2:]}")
